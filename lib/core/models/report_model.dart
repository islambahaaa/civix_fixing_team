class ReportModel {
  final String id;
  final String title;
  final String description;
  final String imageUrl;
  final String location;
  final String date;
  final String city;
  final String status;

  ReportModel({
    required this.id,
    required this.title,
    required this.description,
    required this.imageUrl,
    required this.location,
    required this.date,
    required this.city,
    required this.status,
  });
}
